public class Main {
    public static void main(String[] args) {
        //Escape Sequences (/)
        //Below are the mostly used ones
        //To include "" in the output
        System.out.println("Hello \"world!\"");

        //To print a new line
        System.out.println("Hello \n The World");

        //To print a backslash in the output (\)
        System.out.println("C:\\windows\\cmd\\hello");

        //To print a Tab space
        System.out.println("Hello \t World");
    }
}